class Employee:
    def __init__(self,name,salary):
        self.name = name
        self.salary = salary

satyam = Employee("Satyam",23456789)
print("Salary of Employee1")
print(satyam.name)
print(satyam.salary)

raj = Employee("Raj",2389)
print("\nSalary of Employee1")
print(raj.name)
print(raj.salary)
